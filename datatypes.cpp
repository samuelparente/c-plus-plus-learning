#include <iostream>

int main() {
    int integerNumber = 10;
    float floatNumber = 3.14;
    double doubleNumber = 2.71828;
    char character = 'A';
    bool boolean = true;

    std::cout << "Integer: " << integerNumber << std::endl;
    std::cout << "Float: " << floatNumber << std::endl;
    std::cout << "Double: " << doubleNumber << std::endl;
    std::cout << "Character: " << character << std::endl;
    std::cout << "Boolean: " << boolean << std::endl;

    return 0;
}
